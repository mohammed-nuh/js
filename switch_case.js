let grade = "A";

switch (grade) {
  case "A+":
    console.log("You scored distinction!");
    break;
  case "A":
    console.log("You scored First Class!");
    break;
  case "B+":
    console.log("You scored Second Class!");
    break;
  case "B+":
    console.log("You scored Third Class!");
    break;
  default:
    console.log("You failed!");
    break;
}
